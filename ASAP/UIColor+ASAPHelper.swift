import Foundation
import UIKit

extension UIColor
{
    func asapThemeColor() -> UIColor {
        return UIColor.init(colorLiteralRed: 27.0/255.0, green: 55.0/255.0, blue: 128.0/255.0, alpha: 1.0)
    }
}
